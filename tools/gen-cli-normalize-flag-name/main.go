package main

import (
	"bytes"
	"fmt"
	"io/ioutil"
	"log"
	"os"
	"path/filepath"
	"text/template"

	"github.com/sacloud/usacloud/tools"
)

var (
	destination = "src/github.com/sacloud/usacloud/command/cli"
	ctx         = tools.NewGenerateContext()
)

func Usage() {
	fmt.Fprintf(os.Stderr, "Usage of %s:\n", os.Args[0])
	fmt.Fprint(os.Stderr, "\tgen-cli-normalize-flag-name\n")
	os.Exit(2)
}

func main() {
	log.SetFlags(0)
	log.SetPrefix("gen-cli-normalize-flag-name: ")

	for _, resource := range ctx.Resources {
		filePath := filepath.Join(destination, resource.CLINormalizeFlagsFileName())
		fileFullPath := filepath.Join(ctx.Gopath(), filePath)

		src, err := generateSource(resource)
		if err != nil {
			log.Fatalf("writing output: %s", err)
		}

		err = ioutil.WriteFile(fileFullPath, tools.Sformat([]byte(src)), 0644)
		if err != nil {
			log.Fatalf("writing output: %s", err)
		}
		fmt.Printf("generated: %s\n", filePath)
	}
}

func generateSource(resource *tools.Resource) (string, error) {
	buf := bytes.NewBufferString("")
	t := template.New("t")
	template.Must(t.Parse(srcTemplate))
	err := t.Execute(buf, resource)
	return buf.String(), err
}

var srcTemplate = `// Code generated by 'github.com/sacloud/usacloud/tools/gen-cli-normalize-flag-name'; DO NOT EDIT

package cli

import (
	"github.com/spf13/pflag"
)

{{ range .Commands }}
func {{ .CLINormalizeFlagsFuncName }}(_ *pflag.FlagSet, name string) pflag.NormalizedName {
{{ if .HasLongAliases -}}
	switch name {
{{ range .Params }}{{ $name := .FlagName }}{{ range .LongAliases -}}
	case "{{.}}":
		name = "{{ $name }}"
{{ end }}{{ end -}}
	}
{{ end -}}
	return pflag.NormalizedName(name)
}
{{ end }}
`
