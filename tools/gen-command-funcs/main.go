package main

import (
	"bytes"
	"fmt"
	"io/ioutil"
	"log"
	"os"
	"path/filepath"
	"text/template"

	"github.com/sacloud/usacloud/pkg/schema"
	"github.com/sacloud/usacloud/tools"
	"github.com/sacloud/usacloud/tools/gen-command-funcs/internal"
)

var (
	destination = "src/github.com/sacloud/usacloud/pkg/funcs"
	ctx         = tools.NewGenerateContext()
)

// Usage is a replacement usage function for the flags package.
func Usage() {
	fmt.Fprintf(os.Stderr, "Usage of %s:\n", os.Args[0])
	fmt.Fprintf(os.Stderr, "\tgen-command-funcs\n")
	os.Exit(2)
}

func main() {
	log.SetFlags(0)
	log.SetPrefix("gen-command-funcs: ")

	for _, r := range ctx.Resources {
		err := generateResource(r)
		if err != nil {
			log.Fatalf("writing output: %s", err)
		}
	}
}

func generateResource(resource *tools.Resource) error {

	// build commands
	for _, c := range resource.Commands {
		src, err := generateCommands(c)

		if err != nil {
			return err
		}

		// Write to file.
		// like 'switch_command_list.go'
		baseName := c.CommandFileName()
		outputName := filepath.Join(ctx.Gopath(), destination, baseName)

		// target file is exist?
		_, err = os.Stat(outputName)
		if !c.UseCustomCommand || err != nil {
			err = ioutil.WriteFile(outputName, tools.Sformat([]byte(src)), 0644)
			if err != nil {
				return err
			}
			fmt.Printf("generated: %s\n", filepath.Join(destination, baseName))
		}
	}

	return nil
}

func generateCommands(command *tools.Command) (string, error) {
	b := bytes.NewBufferString("")
	t := template.New("c")
	template.Must(t.Parse(commandTemplate))

	action, err := generateAction(command)
	if err != nil {
		return "", err
	}

	err = t.Execute(b, map[string]interface{}{
		"FuncName":             command.FuncName(),
		"ParamName":            command.InputModelTypeName(),
		"Action":               action,
		"NeedDonotEditComment": !command.UseCustomCommand,
	})

	return b.String(), err
}

var commandTemplate = `{{ if .NeedDonotEditComment }}// Code generated by 'github.com/sacloud/usacloud/tools/gen-command-funcs'; DO NOT EDIT{{ end }}

package funcs

import (
    "fmt"
    "github.com/sacloud/usacloud/pkg/cli"
    "github.com/sacloud/usacloud/pkg/params"
    "github.com/sacloud/usacloud/pkg/utils"
)

func {{.FuncName}}(ctx cli.Context, params *params.{{.ParamName}}) error {
    {{.Action}}
}
`

func generateAction(command *tools.Command) (string, error) {

	var res string
	var err error
	switch command.Type {
	case schema.CommandList:
		// list / find / search
		res, err = internal.GenerateFindCommand(ctx, command)
	case schema.CommandCreate:
		// create
		res, err = internal.GenerateCreateCommand(ctx, command)
	case schema.CommandRead:
		// read
		res, err = internal.GenerateReadCommand(ctx, command)
	case schema.CommandUpdate:
		// update
		res, err = internal.GenerateUpdateCommand(ctx, command)
	case schema.CommandDelete:
		// delete
		res, err = internal.GenerateDeleteCommand(ctx, command)
	case schema.CommandManipulateMulti, schema.CommandManipulateSingle, schema.CommandManipulateIDOnly:
		// power-on/off
		res, err = internal.GenerateManipulateCommand(ctx, command)
	case schema.CommandCustom:
		// custom
		res = `return fmt.Errorf("Not implements")`
	}

	return res, err
}
